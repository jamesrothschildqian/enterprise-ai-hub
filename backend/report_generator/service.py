def _apply_lang(d, suffix: str):
    """Walk dict/list and promote key_{suffix} -> key (multilingual support)"""
    if isinstance(d, dict):
        sk = f"_{suffix}"
        lang_keys = [k for k in d if k.endswith(sk)]
        for lk in lang_keys:
            base = lk[:-len(sk)]
            d[base] = d[lk]
            del d[lk]
        for v in d.values():
            _apply_lang(v, suffix)
    elif isinstance(d, list):
        for item in d:
            _apply_lang(item, suffix)


def generate_report(industry_id: str) -> dict:
    """生成指定行业的落地Case报告"""
    report = INDUSTRY_REPORTS.get(industry_id)
    if not report:
        msg = f"Industry not found: {industry_id}" if get_lang() == "en" else f"未找到行业: {industry_id}"
        return {"error": f"{msg}，可选: {list(INDUSTRY_REPORTS.keys())}"}

    import copy
    result = copy.deepcopy(report)
    lang = get_lang()
    if lang != "zh":
        _apply_lang(result, lang)

    return {
        "industry_id": industry_id,
        "report": result,
        "generated_at": "AI自动生成 · 基于行业真实案例与Mock数据" if get_lang() != "en" else "AI-Generated · Based on real industry cases & mock data",
        "version": "2.0.0",
    }


